package com.emranul.movieinfo.model;

public class Crew {

    private String name;
    private String job;

    public Crew() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }
}
